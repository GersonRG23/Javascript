// Sentencias Switch

let nota = 5;

switch(nota){
    case 5:
        console.log("Re Joya");
        break;
    case 4:
        console.log("Joya");
        break;
    case 3:
        console.log("Medio Joya");
        break;
    case 2:
        console.log("Poco Joya");
        break;
    case 1:
        console.log("Nada Joya");
        break;
    default:
        console.log("Error")
        break;
}